package main

import "fmt"

func main() {
	//	1 => 0000 0001 move 2 steps to the right => 0000 0000 => 0
	var a int = 1 >> 2
	fmt.Println(a)

	// 1 => 0000 0001 move 2 steps to the left => 0000 0100 => 4
	var b int = 1 << 2
	fmt.Println(b)

	// -2 => 1000 0010 => 1111 1101 => 1111 1100 move 2 steps to the left => 1111 0000 => 1110 1111 => 1001 0000 => -16 //	Not sure
	var c int = -2 << 3
	fmt.Println("c", c)

	//	0000 0010
	//	0000 0011
	//	0000 0010 => 2
	fmt.Println(2 & 3)

	//	0000 0010
	//	0000 0011
	//	0000 0011 => 3
	fmt.Println(2 | 3)

	//	0000 0010
	//	0000 0011
	//	0000 0001 => 1
	fmt.Println(2 ^ 3)

	//	-2 => 1000 0010 => 1111 1101 => 1111 1110
	//	 2 => 0000 0010 => 0000 0010 => 0000 0010
	//									1111 1100 => 1111 1011 => 1000 0100 => -4
	fmt.Println(-2 ^ 2)

	//	-2 => 1000 0010 => 1111 1101 => 1111 1110
	//   3 => 0000 0011 => 0000 0011 => 0000 0011
	//									1111 1101 => 1111 1100 => 1000 0011 => -3
	fmt.Println(-2 ^ 3)

}
